<h2>Algemene voorwaarden</h2>
<br />
Goede algemene voorwaarden zijn heel belangrijk, zowel de consument als de webwinkelier worden hierdoor beschermd. Algemene voorwaarden scheppen duidelijkheid over alle rechten en plichten die beide partijen hebben. Dus niet alleen een overzicht van levertijden, betalingsmogelijkheden verzendkosten etc. maar onderwerpen als garantie, overmacht, aansprakelijkheid en Wet verkopen op afstand horen thuis in goede algemene voorwaarden.
 <br /><br />
De klant dient uw algemene voorwaarden te kunnen downloaden in PDF- of ander universeel downloadbaar formaat. Het is niet wettelijk verplicht om de klant accoord te laten gaan met de algemene voorwaarden alvorens een bestelling te plaatsen, bijvoorbeeld d.m.v. het aanvinken van een checkbox in de bestelprocedure.
 <br /><br />
Bij veel websites van webwinkeliers gaat het juist bij het naleven van de informatieplichten mis. Let bij een elektronische overeenkomst op de volgende punten:<br />
Duidelijke stappen<br />
De webwinkelier moet laten zien op welke wijze de elektronische overeenkomst tot stand komt en welke handelingen en stappen nodig zijn om uiteindelijk een bestelling te plaatsen. Daarnaast moet het voor de klant steeds duidelijk zijn in welke stap van het bestelproces hij zich bevindt.<br />
Wel of niet archiveren?<br />
De webwinkelier moet de klant ook informeren of de elektronische overeenkomst wordt gearchiveerd. Is dit het geval, dan is de webwinkelier verplicht de klant te informeren hoe deze overeenkomst kan worden opgevraagd.<br />
Herstel van fouten<br />
Het bestelproces moet zo worden ingericht dat een fout die door een klant wordt gemaakt, nog voor het sluiten van de overeenkomst kan worden hersteld. Bijvoorbeeld door op elke pagina in het proces een ‘terug’ of ‘wijzig’ button op te nemen. Aan het einde van het bestelproces moet een totaaloverzicht van alle ingevulde gegevens worden getoond, zodat de klant de mogelijkheid heeft om invoerfouten te herkennen en herstellen.<br />
Verschillende talen<br />
Als de website in meerdere talen wordt weergegeven, moet de klant tijdens het bestelproces worden geïnformeerd over de talen waarin de overeenkomst kan worden gesloten.<br />
Gedragscodes<br />
Een webwinkelier die een gedragscode hanteert (bijvoorbeeld van Thuiswinkel.org) is verplicht dit tijdens de totstandkoming van de overeenkomst kenbaar te maken en aan te geven op welke wijze de gedragscode is te raadplegen (link naar de betreffende gedragscode).<br />
Voorwaarden<br />
